#!/usr/bin/env node
import { readFileSync, writeFileSync, readdirSync } from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const root = path.join(__dirname, '..')

const index = JSON.parse(readFileSync(path.join(root, 'skills-index.json'), 'utf8'))
const indexedSet = new Set(index)

// 扫描 skills/ 下所有二级目录（category/skill-name 格式）
const skillsDir = path.join(root, 'skills')
const excludePaths = []

for (const category of readdirSync(skillsDir, { withFileTypes: true })) {
  if (!category.isDirectory()) continue
  const categoryPath = path.join(skillsDir, category.name)
  for (const skill of readdirSync(categoryPath, { withFileTypes: true })) {
    if (!skill.isDirectory()) continue
    const rel = `${category.name}/${skill.name}`
    if (!indexedSet.has(rel)) {
      excludePaths.push(`skills/${rel}/`)
    }
  }
}

// 更新 package.json 的 files 字段：将 "skills/" 替换为具体的 skill 路径
const pkgPath = path.join(root, 'package.json')
const pkg = JSON.parse(readFileSync(pkgPath, 'utf8'))

// 移除泛化的 "skills/" 条目，加入具体的索引 skill 路径
const baseFiles = (pkg.files || []).filter(f => f !== 'skills/' && f !== 'skills')
const skillFiles = index.map(rel => `skills/${rel}/`)
pkg.files = [...baseFiles, ...skillFiles, 'skills/.gitkeep']
writeFileSync(pkgPath, JSON.stringify(pkg, null, 2) + '\n')

// 读取现有 .npmignore（保留手写部分）
const npmignorePath = path.join(root, '.npmignore')
const existing = readFileSync(npmignorePath, 'utf8')

// 找到或追加 generated block
const marker = '# generated by scripts/generate-npmignore.js'
const base = existing.includes(marker)
  ? existing.slice(0, existing.indexOf(marker))
  : existing.trimEnd() + '\n\n'

const generated = [marker, ...excludePaths.sort()].join('\n') + '\n'
writeFileSync(npmignorePath, base + generated)

console.log(`Updated .npmignore: excluded ${excludePaths.length} non-indexed skill paths`)
console.log(`Updated package.json files: ${skillFiles.length} indexed skills`)
